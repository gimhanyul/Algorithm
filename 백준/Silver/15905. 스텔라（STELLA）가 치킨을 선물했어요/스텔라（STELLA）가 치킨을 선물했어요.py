def 정렬(입력_리스트):
    # 문제 해결 순이 우선, 패널티 순이 그 다음으로 정렬
    순서대로정렬 = sorted(입력_리스트, key=lambda x: (-x[0], x[1]))
    return 순서대로정렬

결과 = 0
n = int(input())
입력_리스트 = []
for _ in range(n):
    입력값 = input()
    문제수, 패널티 = map(int, 입력값.split())
    입력_리스트.append((문제수, 패널티))  # 정렬 기준에 맞춰서 입력

정렬된_리스트 = 정렬(입력_리스트)

문제_해결_수 = 정렬된_리스트[4][0]

# 5등 이후로 문제 해결 수가 5등과 같은 참가자 수 계산
for i in range(5, n):
    if 정렬된_리스트[i][0] == 문제_해결_수:
        결과 += 1

print(결과)
